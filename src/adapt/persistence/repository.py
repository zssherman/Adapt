    # =========================================================================
    # Utility: Run ID Generation
    # =========================================================================

    @staticmethod
    def generate_run_id(radar: str) -> str:
        """Generate a run ID in YYYYMONDD-HHMM-RADAR format.

        Returns
        -------
        str
            Run identifier, e.g. ``2026MAR23-0206-KBOX``
        """
        now_local = datetime.now()
        month = now_local.strftime("%b").upper()
        return f"{now_local:%Y}{month}{now_local:%d}-{now_local:%H%M}-{radar.upper()}"
