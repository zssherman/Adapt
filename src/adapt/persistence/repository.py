    # Public API: Write Operations
    # =========================================================================

    def write_netcdf(
        self,
        ds: xr.Dataset,
        product_type: str,
        scan_time: datetime,
        producer: str,
        parent_ids: list[str] | None = None,
        metadata: dict | None = None,
        filename_stem: str | None = None
    ) -> str:
        """Write xarray Dataset to NetCDF and register artifact.

        Uses atomic write (temp file + rename) for safety.

        Parameters
        ----------
        ds : xr.Dataset
            Dataset to write
        product_type : str
            Must be GRIDDED_NC or ANALYSIS_NC
        scan_time : datetime
            Scan timestamp
        producer : str
            Component name
        parent_ids : list of str, optional
            Parent artifact IDs
        metadata : dict, optional
            Additional metadata
        filename_stem : str, optional
            Base filename (without extension). Auto-generated if None.

        Returns
        -------
        str
            Registered artifact ID
        """
        # Generate output path
        output_path = self._generate_netcdf_path(
            product_type=product_type,
            scan_time=scan_time,
            filename_stem=filename_stem
        )

        # Atomic write
        self._atomic_write_netcdf(ds, output_path)

        # Register artifact
        return self.register_artifact(
            product_type=product_type,
            file_path=output_path,
            scan_time=scan_time,
            producer=producer,
            parent_ids=parent_ids,
            metadata=metadata
        )

    def write_parquet(
        self,
        df: pd.DataFrame,
        product_type: str,
        scan_time: datetime,
        producer: str,
        parent_ids: list[str] | None = None,
        metadata: dict | None = None,
        filename_stem: str | None = None
    ) -> str:
        """Write DataFrame to Parquet and register artifact.

        Parameters
        ----------
        df : pd.DataFrame
            DataFrame to write
        product_type : str
            Must be CELLS_PARQUET
        scan_time : datetime
            Scan timestamp
        producer : str
            Component name
        parent_ids : list of str, optional
            Parent artifact IDs
        metadata : dict, optional
            Additional metadata
        filename_stem : str, optional
            Base filename

        Returns
        -------
        str
            Registered artifact ID
        """
        # Generate output path
        output_path = self._generate_parquet_path(
            scan_time=scan_time,
            filename_stem=filename_stem
        )

        # Atomic write
        self._atomic_write_parquet(df, output_path)

        # Update metadata with row count
        metadata = metadata or {}
        metadata['row_count'] = len(df)

        # Register artifact
        return self.register_artifact(
            product_type=product_type,
            file_path=output_path,
            scan_time=scan_time,
            producer=producer,
            parent_ids=parent_ids,
            metadata=metadata
        )

    def write_analysis2d_parquet(
        self,
        df: pd.DataFrame,
        scan_time: datetime,
        producer: str = "processor",
        parent_ids: list[str] | None = None,
        metadata: dict | None = None
    ) -> str:
        """Write analysis DataFrame to Parquet file (one per run_id).

        This replaces the old write_sqlite_table for cells data. Data is appended
        to a single Parquet file per run at RADAR_ID/analysis/analysis2d_{run_id}.parquet.

        Parameters
        ----------
        df : pd.DataFrame
            Analysis data (cells) to write
        scan_time : datetime
            Scan timestamp
        producer : str
            Component name (default: "processor")
        parent_ids : list of str, optional
            Parent artifact IDs (e.g., gridded3d items)
        metadata : dict, optional
            Additional metadata

        Returns
        -------
        str
            Registered item ID from RadarCatalog
        """
        # Ensure required columns
        if 'radar' not in df.columns:
            df['radar'] = self.radar
        if 'run_id' not in df.columns:
            df['run_id'] = self.run_id
        if 'scan_time' not in df.columns:
            df['scan_time'] = scan_time

        # Generate parquet file path - one file per run_id
        analysis_dir = self.catalog.radar_dir / "analysis"
        analysis_dir.mkdir(parents=True, exist_ok=True)
        parquet_path = analysis_dir / f"analysis2d_{self.run_id}.parquet"

        # Append to or create parquet file
        try:
            import pyarrow as pa
            import pyarrow.parquet as pq

            # Convert to PyArrow table
            table = pa.Table.from_pandas(df)

            # If file exists, read and append; otherwise create new
            if parquet_path.exists():
                # Use pandas concat to handle schema evolution (new columns fill as NaN)
                existing_df = pq.read_table(parquet_path).to_pandas()
                combined_df = pd.concat([existing_df, df], ignore_index=True)

                # Ensure datetime columns are properly typed (fix concat type coercion)
                datetime_cols = [
                    'time', 'scan_time', 'start_time', 'end_time',
                    'time_volume_start', 'time_volume_end',
                ]
                for col in datetime_cols:
                    if col in combined_df.columns:
                        combined_df[col] = pd.to_datetime(
                            combined_df[col],
                            errors='coerce',
                            utc=True,
                        ).dt.tz_convert(None)

                table = pa.Table.from_pandas(combined_df)
                logger.debug(
                    f"Appended {len(df)} rows to {parquet_path} (schema evolution handled)"
                )

            # Write or overwrite parquet file
            pq.write_table(table, parquet_path)
            logger.debug(f"Wrote {len(table)} total rows to {parquet_path}")
            
        except ImportError:
            # Fallback to pandas (less efficient but works)
            if parquet_path.exists():
                existing_df = pd.read_parquet(parquet_path)
                df = pd.concat([existing_df, df], ignore_index=True)
                logger.debug(f"Appended {len(df)} rows to {parquet_path}")
            
            df.to_parquet(parquet_path, engine='pyarrow', index=False)
            logger.debug(f"Wrote {len(df)} total rows to {parquet_path}")

        # Register in new catalog system
        item_metadata = metadata or {}
        item_metadata['row_count'] = len(df)
        item_metadata['num_cells'] = len(df)
        item_metadata['producer'] = producer  # Store producer in metadata
        
        # Generate unique item ID
        import uuid
        item_id = str(uuid.uuid4())[:16]
        
        item_id = self.catalog.register_item(
            item_id=item_id,
            run_id=self.run_id,
            item_type="analysis2d",
            scan_time=scan_time.isoformat(),
            file_path=str(parquet_path.relative_to(self.catalog.radar_dir)),
            processing_stage="analysis",
            status="complete",
            parent_ids=parent_ids or [],
            metadata=item_metadata
        )
        
        return item_id

    def write_sqlite_table(
        self,
        df: pd.DataFrame,
        table_name: str,
        artifact_id: str,
        if_exists: str = 'append'
    ) -> None:
        """Write DataFrame to SQLite table within an existing artifact.

        Performs automatic schema migration when appending: if the DataFrame
        has columns not present in the existing table, those columns are
        added via ALTER TABLE before the insert.

        Parameters
        ----------
        df : pd.DataFrame
            Data to write
        table_name : str
            Table name
        artifact_id : str
            Existing CELLS_DB artifact ID
        if_exists : str
            Pandas to_sql behavior ('append', 'replace', 'fail')
        """
        artifact = self._get_artifact(artifact_id)

        if artifact is None:
            raise ValueError(f"Artifact not found: {artifact_id}")

        file_path = Path(artifact['file_path'])

        with sqlite3.connect(str(file_path)) as conn:
            # If appending, perform schema migration for missing columns
            if if_exists == 'append':
                self._migrate_table_schema(conn, table_name, df)

            df.to_sql(table_name, conn, if_exists=if_exists, index=False)

        logger.debug(f"Wrote {len(df)} rows to {table_name} in {artifact_id}")

    def _migrate_table_schema(
        self,
        conn: sqlite3.Connection,
        table_name: str,
        df: pd.DataFrame
    ) -> None:
        """Add missing columns to existing table before append.

        Uses the same type mapping rules as processor._create_cells_table:
        - *_x, *_y suffix → INTEGER (pixel coordinates)
        - *_lat, *_lon suffix → REAL (geographic coordinates)
        - *_json suffix → TEXT (JSON data)
        - radar_* prefix → REAL (radar measurements)
        - *_mean, *_min, *_max, *_sqkm suffix → REAL (statistics)
        - time* columns → TIMESTAMP
        - cell_label → INTEGER
        - Default → TEXT
        """
        # Check if table exists
        cursor = conn.execute(
            "SELECT name FROM sqlite_master WHERE type='table' AND name=?",
            (table_name,)
        )
        if cursor.fetchone() is None:
            # Table doesn't exist yet, will be created by to_sql
            return

        # Get existing columns
        cursor = conn.execute(f"PRAGMA table_info('{table_name}')")
        existing_columns = {row[1] for row in cursor.fetchall()}

        # Find missing columns
        df_columns = set(df.columns)
        missing_columns = df_columns - existing_columns

        if not missing_columns:
            return

        # Add missing columns
        for col in missing_columns:
            sql_type = self._infer_sql_type(col)
            try:
                conn.execute(f'ALTER TABLE "{table_name}" ADD COLUMN "{col}" {sql_type}')
                logger.debug(f"Added column '{col}' ({sql_type}) to table '{table_name}'")
            except sqlite3.OperationalError as e:
                # Column might already exist (race condition)
                if "duplicate column name" not in str(e).lower():
                    raise

        conn.commit()
        logger.info(f"Schema migration: added {len(missing_columns)} columns to '{table_name}'")

    @staticmethod
    def _infer_sql_type(column_name: str) -> str:
        """Infer SQL type from column name using processor schema conventions.

        Type mapping rules (matching processor._create_cells_table):
        - Pixel coordinates (*_x, *_y) → INTEGER
        - Geographic coordinates (*_lat, *_lon) → REAL
        - JSON fields (*_json) → TEXT
        - Radar measurements (radar_*) → REAL
        - Statistics (*_mean, *_min, *_max, *_sqkm) → REAL
        - Timestamps (time*) → TIMESTAMP
        - Cell identifier (cell_label) → INTEGER
        - Default → TEXT
        """
        col = column_name.lower()

        # Integer types
        if col.endswith('_x') or col.endswith('_y'):
            return 'INTEGER'
        if col == 'cell_label':
            return 'INTEGER NOT NULL'

        # Real/float types
        if col.endswith('_lat') or col.endswith('_lon'):
            return 'REAL'
        if col.startswith('radar_'):
            return 'REAL'
        if col.endswith(('_mean', '_min', '_max', '_sqkm')):
            return 'REAL'
        if 'heading' in col:
            return 'REAL'
        if 'area' in col:
            return 'REAL'

        # Timestamp types
        if col.startswith('time'):
            return 'TIMESTAMP'

        # Text types
        if col.endswith('_json'):
            return 'TEXT'

        # Default to TEXT for safety
        return 'TEXT'

    def get_or_create_cells_db(
        self,
        scan_time: datetime,
        producer: str,
        parent_ids: list[str] | None = None
    ) -> str:
        """Get existing cells database or create new one.

        Parameters
        ----------
        scan_time : datetime
            Scan timestamp (used for path generation)
        producer : str
            Component name
        parent_ids : list of str, optional
            Parent artifact IDs

        Returns
        -------
        str
            Artifact ID of cells database
        """
        # Check for existing
        existing = self.query(product_type=ProductType.CELLS_DB)

        if existing:
            return existing[0]['artifact_id']

        # Create new
        output_path = self._generate_sqlite_path(scan_time)

        # Create empty database with WAL mode
        self._init_cells_db(output_path)

        return self.register_artifact(
            product_type=ProductType.CELLS_DB,
            file_path=output_path,
            scan_time=scan_time,
            producer=producer,
            parent_ids=parent_ids or [],
            metadata={}
        )

