class ProductType:
    """Artifact product type constants."""
    NEXRAD_RAW = "nexrad_raw"
    GRIDDED_NC = "gridded3d"  # Updated to match new catalog
    ANALYSIS_NC = "segmentation2d"  # Updated to match new catalog
    CELLS_DB = "cells_db"  # DEPRECATED - use ANALYSIS2D_PARQUET
    CELLS_PARQUET = "cells_parquet"  # DEPRECATED - use ANALYSIS2D_PARQUET
    ANALYSIS2D_PARQUET = "analysis2d"  # NEW: Standard name for analysis tables
    PLOT_PNG = "plot_png"


class DataRepository:
    """Centralized artifact management for radar processing pipeline.

    Manages the complete lifecycle of pipeline artifacts:
    - Registration in per-run SQLite catalog
    - Atomic file writes with lineage tracking
    - Processing status management
    - Query API for downstream consumers

    Directory Structure:
        root_dir/
        ├── catalog/{run_id}_pipeline_catalog.db
        ├── RADAR_ID/
        │   ├── nexrad/YYYYMMDD/{original_aws_name}
        │   ├── gridnc/YYYYMMDD/{file_stem}_{run_id}_gridded.nc
        │   ├── analysis/YYYYMMDD/{file_stem}_{run_id}_analysis.nc
        │   ├── analysis/YYYYMMDD/{radar}_{run_id}_cells.db
        │   └── plots/YYYYMMDD/{radar}_{plot_type}_{HHMMSS}_{run_id}.png
        └── logs/

    Thread Safety:
        All methods are thread-safe via internal locking.
        Multiple threads can register and query artifacts concurrently.

    Example usage::

        # Create repository for a pipeline run
        repo = DataRepository(
            run_id="abc12345",
            base_dir="/data/radar_output",
            radar="KDIX"
        )

        # Write gridded NetCDF with lineage
        gridded_id = repo.write_netcdf(
            ds=gridded_dataset,
            product_type=ProductType.GRIDDED_NC,
            scan_time=datetime(2026, 2, 11, 12, 0, 0),
            producer="loader",
            parent_ids=[raw_artifact_id],
            metadata={"grid_shape": [41, 201, 201]}
        )

        # Query artifacts
        grids = repo.query(product_type=ProductType.GRIDDED_NC)
    """

    def __init__(
        self,
        run_id: str,
        base_dir: str | Path,
        radar: str,
        config: Optional["InternalConfig"] = None,
    ):
        """Initialize repository for a pipeline run.

        Parameters
        ----------
        run_id : str
            Unique run identifier for this pipeline run.
            Generated by orchestrator at startup.
        base_dir : str or Path
            Root output directory for all artifacts.
        radar : str
            Radar station identifier (e.g., "KDIX").
        config : InternalConfig, optional
            Validated runtime configuration.
        """
        self.run_id = run_id
        self.base_dir = Path(base_dir).resolve()
        self.radar = radar
        self.config = config

        # Create directory structure FIRST (radar_catalog needs the dir to exist)
        self._init_directories()

        # Catalog system
        self.registry = RepositoryRegistry.get_instance(self.base_dir)
        self.catalog = RadarCatalog(self.base_dir / radar)

        # Thread safety
        self._lock = threading.RLock()

        # Register this run in catalog and registry
        self._register_in_new_catalog()

        logger.debug("DataRepository ready: run=%s radar=%s", run_id, radar)

    def _init_directories(self) -> None:
        """Create directory structure for this radar."""
        radar_base = self.base_dir / self.radar

        self.output_dirs = {
            "base": self.base_dir,
            "nexrad": radar_base / "nexrad",
            "gridnc": radar_base / "gridnc",
            "analysis": radar_base / "analysis",
            "plots": radar_base / "plots",
            "logs": self.base_dir / "logs",
        }

        for path in self.output_dirs.values():
            path.mkdir(parents=True, exist_ok=True)

    # =========================================================================
    # Catalog Registration
    # =========================================================================

    def _register_in_new_catalog(self) -> None:
        """Register this run in the new Registry and RadarCatalog system."""
        # Register radar in global registry (idempotent), including optional location metadata.
        radars = self.registry.list_radars()
        row = radars[radars["radar"] == self.radar] if not radars.empty else None
        existing_lat = row.iloc[0]["location_lat"] if row is not None and not row.empty else None
        existing_lon = row.iloc[0]["location_lon"] if row is not None and not row.empty else None

        # Do not use external lookup tables for location metadata. The pipeline
        # will populate location_lat/location_lon from the first ingested radar file.
        self.registry.register_radar(self.radar, lat=existing_lat, lon=existing_lon)
        
        # Save runtime config to radar directory first
        config_path = None
        if self.config:
            config_file = self.catalog.radar_dir / f"config_run_{self.run_id}.json"
            if not config_file.exists():
                config_json = self.config.model_dump_json()
                with open(config_file, 'w') as f:
                    f.write(config_json)
                logger.debug(f"Saved runtime config: {config_file}")
            config_path = str(config_file.relative_to(self.base_dir))
        
        # Register this run in global registry
        mode = self.config.mode if self.config else "unknown"
        
        self.registry.register_run(
            run_id=self.run_id,
            radar=self.radar,
            mode=mode,
            config_path=config_path,
            repository_version="0.1.0"
        )

    # =========================================================================
    # Artifact ID Generation
    # =========================================================================

    @staticmethod
    def generate_artifact_id(
        product_type: str,
        radar: str,
        scan_time: datetime | None,
        run_id: str,
        content_hint: str = ""
    ) -> str:
        """Generate deterministic artifact ID from content metadata.

        Parameters
        ----------
        product_type : str
            Type of artifact (e.g., ProductType.GRIDDED_NC)
        radar : str
            Radar station identifier
        scan_time : datetime or None
            Timestamp of radar scan
        run_id : str
            Pipeline run identifier
        content_hint : str, optional
            Additional content for uniqueness (e.g., file hash)

        Returns
        -------
        str
            16-character hash-based artifact ID
        """
        time_str = scan_time.isoformat() if scan_time else "none"
        content = f"{product_type}:{radar}:{time_str}:{run_id}:{content_hint}"
        return hashlib.sha256(content.encode()).hexdigest()[:16]

    # =========================================================================
    # Public API: Registration
    # =========================================================================

    def register_artifact(
        self,
        product_type: str,
        file_path: str | Path,
        scan_time: datetime | None = None,
        producer: str = "unknown",
        parent_ids: list[str] | None = None,
        metadata: dict | None = None
    ) -> str:
        """Register an artifact in the RadarCatalog.

        Parameters
        ----------
        product_type : str
            Artifact type (use ProductType constants)
        file_path : str or Path
            Absolute path to artifact file
        scan_time : datetime, optional
            Timestamp of radar scan
        producer : str
            Name of component that created this artifact
        parent_ids : list of str, optional
            Artifact IDs of parent artifacts (for lineage)
        metadata : dict, optional
            Additional metadata (stored as JSON)

        Returns
        -------
        str
            Unique artifact ID
        """
        file_path = Path(file_path).resolve()
        parent_ids = parent_ids or []
        metadata = metadata or {}

        artifact_id = self.generate_artifact_id(
            product_type=product_type,
            radar=self.radar,
            scan_time=scan_time,
            run_id=self.run_id,
            content_hint=str(file_path)
        )

        radar_dir = self.base_dir / self.radar
        try:
            relative_path = file_path.relative_to(radar_dir)
        except ValueError:
            relative_path = file_path

        file_size = file_path.stat().st_size if file_path.exists() else None

        catalog_metadata = metadata.copy()
        catalog_metadata['producer'] = producer

        self.catalog.register_item(
            item_id=artifact_id,
            run_id=self.run_id,
            item_type=product_type,
            scan_time=scan_time.isoformat() if scan_time else None,
            file_path=str(relative_path),
            parent_ids=parent_ids,
            metadata=catalog_metadata,
            file_size_bytes=file_size,
        )
        logger.debug("Registered artifact: %s (%s)", artifact_id, product_type)
        return artifact_id

    # =========================================================================
    # Public API: Data Access
    # =========================================================================

    def open_dataset(self, artifact_id: str) -> xr.Dataset:
        """Open NetCDF artifact as xarray Dataset.

        Parameters
        ----------
        artifact_id : str
            Artifact ID (must be NetCDF type)

        Returns
        -------
        xr.Dataset
            Opened dataset (caller responsible for closing)

        Raises
        ------
        ValueError
            If artifact not found or not a NetCDF type
        FileNotFoundError
            If file does not exist
        """
        artifact = self._get_artifact(artifact_id)

        if artifact is None:
            raise ValueError(f"Artifact not found: {artifact_id}")

        product_type = artifact['product_type']
        if product_type not in (ProductType.GRIDDED_NC, ProductType.ANALYSIS_NC):
            raise ValueError(f"Cannot open as dataset: {product_type}")

        file_path = Path(artifact['file_path'])
        if not file_path.exists():
            raise FileNotFoundError(f"File not found: {file_path}")

        return xr.open_dataset(file_path)

    def open_table(
        self,
        artifact_id: str,
        table_name: str | None = None
    ) -> pd.DataFrame:
        """Open SQLite or Parquet artifact as DataFrame.

        Parameters
        ----------
        artifact_id : str
            Artifact ID (must be CELLS_DB or CELLS_PARQUET type)
        table_name : str, optional
            Table name for SQLite (default: 'cells')

        Returns
        -------
        pd.DataFrame
            Data from artifact

        Raises
        ------
        ValueError
            If artifact not found or invalid type
        """
        artifact = self._get_artifact(artifact_id)

        if artifact is None:
            raise ValueError(f"Artifact not found: {artifact_id}")

        product_type = artifact['product_type']
        file_path = Path(artifact['file_path'])

        if not file_path.exists():
            raise FileNotFoundError(f"File not found: {file_path}")

        if product_type == ProductType.CELLS_PARQUET:
            return pd.read_parquet(file_path)
        elif product_type == ProductType.CELLS_DB:
            table_name = table_name or 'cells'
            with sqlite3.connect(str(file_path)) as conn:
                return pd.read_sql(f"SELECT * FROM {table_name}", conn)
        else:
            raise ValueError(f"Cannot open as table: {product_type}")

    def query(
        self,
        product_type: str | None = None,
        time_range: tuple[datetime, datetime] | None = None,
        radar: str | None = None,
        limit: int | None = None
    ) -> list[dict]:
        """Query artifacts by criteria.

        Parameters
        ----------
        product_type : str, optional
            Filter by product type
        time_range : tuple of datetime, optional
            (start_time, end_time) filter
        radar : str, optional
            Ignored — repository is already scoped to one radar
        limit : int, optional
            Maximum results

        Returns
        -------
        list of dict
            Matching artifacts with absolute file_path resolved
        """
        df = self.catalog.query_items(
            item_type=product_type, run_id=self.run_id, order_by="scan_time ASC"
        )
        if df.empty:
            return []

        if time_range:
            t0 = time_range[0].isoformat()
            t1 = time_range[1].isoformat()
            df = df[(df["scan_time"] >= t0) & (df["scan_time"] <= t1)]

        if limit:
            df = df.head(limit)

        return [self._normalize_item(row) for _, row in df.iterrows()]

    def get_latest(
        self,
        product_type: str,
        radar: str | None = None
    ) -> dict | None:
        """Get the most recent artifact of a given type.

        Parameters
        ----------
        product_type : str
            Artifact type to query (use ProductType constants)
        radar : str, optional
            Ignored — repository is already scoped to one radar

        Returns
        -------
        dict or None
            Most recent artifact record, or None if no artifacts exist
        """
        item = self.catalog.get_latest_item(item_type=product_type, run_id=self.run_id)
        return self._normalize_item(item) if item else None

    def get_all_since(
        self,
        product_type: str,
        since_artifact_id: str | None = None,
        radar: str | None = None
    ) -> list[dict]:
        """Get all artifacts of a type created after a given artifact.

        Parameters
        ----------
        product_type : str
            Artifact type to query
        since_artifact_id : str, optional
            Return artifacts with scan_time after this artifact's scan_time.
            If None, returns all artifacts of this type.
        radar : str, optional
            Ignored — repository is already scoped to one radar

        Returns
        -------
        list of dict
            Artifacts in chronological order (oldest first)
        """
        df = self.catalog.query_items(item_type=product_type, run_id=self.run_id)
        if df.empty:
            return []

        if since_artifact_id:
            ref = self._get_artifact(since_artifact_id)
            if ref and ref.get("scan_time"):
                df = df[df["scan_time"] > ref["scan_time"]]

        df = df.sort_values("scan_time", ascending=True)
        return [self._normalize_item(row) for _, row in df.iterrows()]

    def get_artifact(self, artifact_id: str) -> dict | None:
        """Get artifact record by ID.

        Parameters
        ----------
        artifact_id : str
            Artifact ID to retrieve

        Returns
        -------
        dict or None
            Artifact record with absolute file_path, or None if not found
        """
        return self._get_artifact(artifact_id)

    def _normalize_item(self, item) -> dict:
        """Convert a RadarCatalog item row to a repository artifact dict.

        Resolves relative file_path to absolute, aliases item_id → artifact_id,
        and hoists producer from the metadata JSON for backward compatibility.
        """
        d = item.to_dict() if hasattr(item, 'to_dict') else dict(item)
        # Resolve relative path stored in catalog to absolute
        rel = d.get("file_path", "")
        if rel and not Path(rel).is_absolute():
            d["file_path"] = str(self.base_dir / self.radar / rel)
        # Alias item_id → artifact_id
        if "artifact_id" not in d:
            d["artifact_id"] = d.get("item_id", "")
        # Alias item_type → product_type
        if "product_type" not in d:
            d["product_type"] = d.get("item_type", "")
        # Hoist producer from metadata JSON for backward compatibility
        meta_raw = d.get("metadata")
        if meta_raw and isinstance(meta_raw, str):
            try:
                meta = json.loads(meta_raw)
                if "producer" not in d and "producer" in meta:
                    d["producer"] = meta["producer"]
            except (json.JSONDecodeError, TypeError):
                pass
        return d

